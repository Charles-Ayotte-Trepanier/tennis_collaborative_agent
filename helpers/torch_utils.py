from .config import device
import torch
import numpy as np
import os


def tensor(x):
    if isinstance(x, torch.Tensor):
        return x
    x = np.asarray(x, dtype=np.float32)
    x = torch.from_numpy(x).to(device)
    return x


def to_np(t):
    return t.cpu().detach().numpy()